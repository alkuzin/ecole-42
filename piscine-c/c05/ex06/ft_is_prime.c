int ft_is_prime(int nb);

int ft_is_prime(int nb)
{
    if(nb < 2)
        return 0;
    int i;

    i = 2;
    while(i < (nb/2) + 1)
    {
        if(nb % i == 0)
            return 0;
        i++;
    }
    return 1;
}
