int ft_sqrt(int nb);

int ft_sqrt(int nb)
{
    if(nb <= 0)
        return 0;

    int i;

    i = 0;
    while(i < nb)
    {
        if(i*i == nb)
            return i;            
        i++;
    }
    return 0;
}
